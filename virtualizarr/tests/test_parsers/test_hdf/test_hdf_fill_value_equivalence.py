def test_write_hdf5_basic_roundtrip(tmp_path):
    """_write_hdf5 writes data, dataset.fillvalue, _FillValue attr,
    and dim scale that h5py can read back."""
    url = _write_hdf5(
        tmp_path / "data.nc",
        dtype=np.dtype("f8"),
        data=np.array([1.0, 2.0, 3.0], dtype="f8"),
        dataset_fillvalue=-9999.0,
        fill_value_attr=-9999.0,
        chunks=(2,),
    )
    assert url.startswith("file://")

    with h5py.File(url.removeprefix("file://"), "r") as f:
        dset = f["data"]
        np.testing.assert_array_equal(dset[:], [1.0, 2.0, 3.0])
        assert dset.fillvalue == -9999.0
        assert dset.attrs["_FillValue"] == -9999.0
        assert dset.chunks == (2,)
        assert len(dset.dims[0]) == 1
        assert dset.dims[0][0].name.lstrip("/") == "dim_0"


def test_write_hdf5_unset_skips_fillvalue(tmp_path):
    """When dataset_fillvalue=_UNSET, h5py uses its dtype default;
    when fill_value_attr=_UNSET, no _FillValue attribute is written.
    """
    url = _write_hdf5(
        tmp_path / "data.nc",
        dtype=np.dtype("i4"),
        data=np.array([1, 2, 3], dtype="i4"),
        # both fill axes left UNSET
    )
    with h5py.File(url.removeprefix("file://"), "r") as f:
        assert "_FillValue" not in f["data"].attrs


