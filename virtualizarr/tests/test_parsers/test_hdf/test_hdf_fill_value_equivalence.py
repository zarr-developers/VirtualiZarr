"""Property-based equivalence tests for the HDF5 parser's fill-value handling.

Each test writes an HDF5 file via h5py, opens it both via xarray+h5netcdf and
via xarray+virtualizarr (manifest_store_from_hdf_url), and asserts the two
loaded datasets are identical at two layers:

  1. raw attribute layer (decode_cf=False) — exposes metadata bugs that CF
     decoding would otherwise mask (e.g. dropped attributes, wrong fill-value
     encoding, lost _Unsigned).
  2. decoded data layer (decode_cf=True) — the user-facing correctness
     check (mask_and_scale, _FillValue masking, scale_factor/add_offset,
     _Unsigned reinterpretation).

See docs/custom_parsers.md ("Fill values" and "Packing and scaling" sections)
for the rules these tests exercise.

The strategies in TestBasicEquivalence / TestPackedEquivalence /
TestUnsignedEquivalence generate the *full* parameter range that
`docs/custom_parsers.md` says the parser should support. Assertions are
plain `_assert_h5netcdf_virtualizarr_identical` (raw-attribute +
decoded-data) — no `xfail`, no `pytest.raises` adapter for cases that
don't work today.

This is intentional: the suite functions as an executable specification.
Failures are TODO items. Suite green = HDFParser stack matches spec.

Compound/structured dtypes can't be generated by the uniform-dtype
basic strategy and are covered by curated cases in `TestCompoundDtype`.

Shared infrastructure (sentinel, hypothesis profile registration,
equivalence assertion helpers, generic numeric-dtype strategy) lives in
`virtualizarr/tests/test_parsers/_fill_value_common.py` so other
parsers can reuse it without copy-paste.

CI throttling:
- VIRTUALIZARR_HYPOTHESIS_PROFILE=ci      -> max_examples=10  (faster)
- VIRTUALIZARR_HYPOTHESIS_PROFILE=thorough -> max_examples=50  (default)
- pytest -m "not hypothesis_tests"        -> skip this module entirely
"""

