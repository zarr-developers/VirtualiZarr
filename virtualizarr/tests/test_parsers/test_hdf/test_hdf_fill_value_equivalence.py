# asserts the precise failure mode, so a change there is detected.
# No separate test class needed.


@st.composite
def _packed_dataset_strategy(draw) -> _DatasetSpec:
    """Packed-data scenarios: integer storage + scale_factor + add_offset
    + _FillValue in the packed (encoded) domain.

    Per docs/custom_parsers.md "Fill values in packed data": the
    _FillValue must be in the packed domain; xarray applies CFMaskCoder
    before CFScaleOffsetCoder on decode.
    """
    dtype = draw(
        st.sampled_from(
            [
                np.dtype("i2"),
                np.dtype("i4"),
                np.dtype("u2"),
                np.dtype("u4"),
            ]
        )
    )
    scale_factor = draw(
        st.floats(min_value=1e-3, max_value=1e3, allow_nan=False, allow_infinity=False)
    )
    add_offset = draw(
        st.floats(min_value=-1e3, max_value=1e3, allow_nan=False, allow_infinity=False)
    )
    shape = draw(npst.array_shapes(min_dims=1, max_dims=3, min_side=1, max_side=8))
    data = draw(npst.arrays(dtype=dtype, shape=shape))
    fill_value_attr = draw(_value_in_dtype_strategy(dtype))
    return _DatasetSpec(
        dtype=dtype,
        data=data,
        fill_value_attr=fill_value_attr,
        scale_factor=scale_factor,
        add_offset=add_offset,
    )


