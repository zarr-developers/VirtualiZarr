@requires_hdf5plugin
@requires_imagecodecs
class TestBasicEquivalence:
    """Equivalence sweep across the dtype × fill matrix.

    The random-draw strategy generates the *full* parameter range that
    `docs/custom_parsers.md` says the parser should support — including
    string-kind dtypes with explicit `_FillValue`, vlen-utf-8 with and
    without fill, etc.

    Curated examples and random draws are **separate test methods** so
    pytest-xdist can parallelise the curated cases and a single failing
    example doesn't shadow the others. Each curated example shows as
    its own test ID (`test_equivalence_curated[<name>]`); random draws
    aggregate under `test_equivalence_random`.

    The assertion is plain `_assert_h5netcdf_virtualizarr_identical`.
    Cases the parser/stack doesn't handle correctly today **will fail**;
    those failures are the to-fix list. Suite green = fixes complete.
    """

    @pytest.mark.parametrize("spec", _BASIC_EXAMPLES, ids=_BASIC_EXAMPLE_IDS)
    def test_equivalence_curated(self, spec, tmp_path):
        filepath = _spec_to_url(spec, tmp_path)
        _assert_h5netcdf_virtualizarr_identical(filepath)

    # tmp_path is function-scoped, but each hypothesis example writes the
    # same `data.nc` and reads it before the next example runs. The
    # overwrite-in-place is intentional.
    @settings(suppress_health_check=[HealthCheck.function_scoped_fixture])
    @given(spec=_basic_dataset_strategy())
    def test_equivalence_random(self, spec, tmp_path):
        filepath = _spec_to_url(spec, tmp_path)
        _assert_h5netcdf_virtualizarr_identical(filepath)


# String-dtype known failures are now covered by _BASIC_EXAMPLES (curated
