# Extra-CF-attribute scenarios: xarray does not specially process these
# attributes; the parser MUST round-trip them as-is. The raw-attrs
# invariant (decode_cf=False) is what surfaces a drop or transformation
# directly; the decoded-data invariant covers the user-facing path.
_EXTRA_ATTR_EXAMPLES = [
    # missing_value alongside _FillValue
    _DatasetSpec(
        dtype=np.dtype("f4"),
        data=np.array([1.0, 2.0, 3.0], dtype="f4"),
        fill_value_attr=np.float32(-9999.0),
        extra_attrs={"missing_value": np.float32(-9998.0)},
    ),
    # valid_range as a 2-element array
    _DatasetSpec(
        dtype=np.dtype("i2"),
        data=np.array([10, 20, 30], dtype="i2"),
        extra_attrs={"valid_range": np.array([0, 100], dtype="i2")},
    ),
    # valid_min / valid_max as separate scalars
    _DatasetSpec(
        dtype=np.dtype("f8"),
        data=np.array([0.5, 1.0, 1.5], dtype="f8"),
        extra_attrs={"valid_min": 0.0, "valid_max": 2.0},
    ),
    # missing_value + valid_range together
    _DatasetSpec(
        dtype=np.dtype("i4"),
        data=np.array([1, 2, 3], dtype="i4"),
        fill_value_attr=np.int32(-1),
        extra_attrs={
            "missing_value": np.int32(-2),
            "valid_range": np.array([0, 1000], dtype="i4"),
        },
    ),
]


