@dataclass
class _DatasetSpec:
    """A self-contained description of one HDF5 file to write for testing.

    Every field passed to _write_hdf5 by tests should land in this dataclass
    so failures shrink to a single repr-able value.
    """

    dtype: np.dtype
    data: np.ndarray
    dataset_fillvalue: Any = _UNSET
    fill_value_attr: Any = _UNSET
    scale_factor: float | None = None
    add_offset: float | None = None
    unsigned: bool = False
    chunks: tuple[int, ...] | None = None
    # Free-form attributes written verbatim to the dataset. Used to cover
    # CF attributes that xarray passes through without special processing
    # (missing_value, valid_range, valid_min, valid_max). The raw-attrs
    # invariant catches any drop automatically.
    extra_attrs: dict[str, Any] | None = None


def _write_hdf5(
    filepath: Path | str,
    *,
    dtype: np.dtype,
    data: np.ndarray,
    dataset_fillvalue: Any = _UNSET,
    fill_value_attr: Any = _UNSET,
    scale_factor: float | None = None,
    add_offset: float | None = None,
    unsigned: bool = False,
    chunks: tuple[int, ...] | None = None,
    extra_attrs: dict[str, Any] | None = None,
) -> str:
    """Write a single-variable HDF5 file with the requested fill-value shape.

    Returns the file:// URL for use with manifest_store_from_hdf_url.
    """
    filepath = str(filepath)

    create_kwargs: dict[str, Any] = {"name": "data", "data": data, "dtype": dtype}
    if chunks is not None:
        create_kwargs["chunks"] = chunks
    if dataset_fillvalue is not _UNSET:
        create_kwargs["fillvalue"] = dataset_fillvalue

    with h5py.File(filepath, "w") as f:
        dset = f.create_dataset(**create_kwargs)
        if fill_value_attr is not _UNSET:
            dset.attrs["_FillValue"] = fill_value_attr
        if scale_factor is not None:
            dset.attrs["scale_factor"] = scale_factor
        if add_offset is not None:
            dset.attrs["add_offset"] = add_offset
        if unsigned:
            dset.attrs["_Unsigned"] = "true"
        if extra_attrs is not None:
            for k, v in extra_attrs.items():
                dset.attrs[k] = v
        # Attach a real dim scale to each axis so xarray+h5netcdf and the
        # HDF parser both produce named "dim_<i>" dims instead of
        # phony_dim_0 — keeps equivalence assertions about fill values,
        # not dim naming.
        for axis, size in enumerate(data.shape):
            scale_name = f"dim_{axis}"
            dim = f.create_dataset(scale_name, data=np.arange(size))
            dim.make_scale(scale_name)
            dset.dims[axis].attach_scale(dim)

    return f"file://{filepath}"


def _open_virtualizarr(filepath: str, *, decode_cf: bool) -> xr.Dataset:
    """Open `filepath` via virtualizarr's HDFParser (manifest store) + xarray's
    zarr backend. Plugged into the shared equivalence helpers as the
    `open_observed` callback.
    """
    manifest_store = manifest_store_from_hdf_url(f"file://{filepath}")
    return xr.open_dataset(
        manifest_store,
        engine="zarr",
        consolidated=False,
        zarr_format=3,
        decode_cf=decode_cf,
    )


def _open_h5netcdf(filepath: str, *, decode_cf: bool) -> xr.Dataset:
    """Open `filepath` via xarray+h5netcdf. The reference engine for the HDF
    equivalence sweep — what a user expects to see when reading the file
    directly without virtualizarr.
    """
    return xr.open_dataset(filepath, engine="h5netcdf", decode_cf=decode_cf)


def _assert_h5netcdf_virtualizarr_identical(filepath: str) -> None:
    """Run both raw-attribute and decoded-data invariants against the HDF
    parser. Thin wrapper around the shared helper that binds the engines.
    """
    assert_equivalent(
        filepath,
        open_observed=_open_virtualizarr,
        open_reference=_open_h5netcdf,
    )


