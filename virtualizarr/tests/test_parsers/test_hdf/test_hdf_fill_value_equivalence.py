@st.composite
def _unsigned_dataset_strategy(draw) -> _DatasetSpec:
    """Signed-integer storage with _Unsigned="true" attribute.

    Per docs/custom_parsers.md "_Unsigned": fill value is interpreted in
    the unsigned domain; e.g. _FillValue=-1 stored as int16 corresponds
    to 65535 when reinterpreted as uint16.
    """
    dtype = draw(
        st.sampled_from(
            [
                np.dtype("i1"),
                np.dtype("i2"),
                np.dtype("i4"),
                np.dtype("i8"),
            ]
        )
    )
    shape = draw(npst.array_shapes(min_dims=1, max_dims=3, min_side=1, max_side=8))
    data = draw(npst.arrays(dtype=dtype, shape=shape))
    fill_value_attr = draw(_value_in_dtype_strategy(dtype))
    return _DatasetSpec(
        dtype=dtype,
        data=data,
        fill_value_attr=fill_value_attr,
        unsigned=True,
    )


